CREATE TABLE ABONENT (
    ID_ABON NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    MSISDN NUMBER(15) NOT NULL,
    CONSTRAINT ABON_PK PRIMARY KEY (ID_ABON)
);

CREATE TABLE CARD (
    ID_CARD NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    PAN NUMBER(15) NOT NULL,
    EXPIRE_DATE DATE NOT NULL,
    ID_ABON NUMBER NOT NULL,
    CONSTRAINT CARD_PK PRIMARY KEY (ID_CARD),
    CONSTRAINT CARD_FK FOREIGN KEY (ID_ABON) REFERENCES ABONENT (ID_ABON) ON DELETE CASCADE 
);
    